function [A, B, D, C] = get_matrices(M, m, l, g)
    A = [0, 1, 0, 0;
        0, 0, (3 * m * g) / (4 * M + m), 0;
        0, 0, 0, 1;
        0, 0, 3 * g * (M + m) / (l * (4 * M + m)), 0];
    B = [0; 4 / (4 * M + m); 0; 3 / (l * (4 * M + m))];
    D = [0; 3 / (l * (4 * M + m)); 0; 3 * (M + m) / ((m * l * l) * (4 * M + m))];
    C = [1, 0, 0, 0; 0, 0, 1, 0];
end